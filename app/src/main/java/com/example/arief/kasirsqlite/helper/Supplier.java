package com.example.arief.kasirsqlite.helper;

public class Supplier {
    public String id;
    public String namaSuppier;
    public String alamatSupplier;
    public String noTelp;

    public Supplier(String id, String namaSuppier, String alamatSupplier, String noTelp) {
        this.id = id;
        this.namaSuppier = namaSuppier;
        this.alamatSupplier = alamatSupplier;
        this.noTelp = noTelp;
    }
}